import java.util.PrimitiveIterator;

public class mathClass {
    public static void main(String[] args) {

        System.out.println(Math.PI); // importa o valor de PI
        System.out.println(Math.E);

        double resultado;

        resultado = Math.pow(2, 5); // potenciação (2^3)
        resultado = Math.abs(-5); // valor absoluto
        resultado = Math.sqrt(9); // raiz quadrada
        resultado = Math.round(3.14); // arredondar
        resultado = Math.ceil(3.14); // teto
        resultado = Math.floor(3.99); // chão
        resultado = Math.max(10, 20); // valor máximo entre os numeros
        resultado = Math.min(10, 20); // valor mínimo entre os numeros


        System.out.println(resultado);

    }
}
