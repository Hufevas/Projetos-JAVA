import java.util.Scanner;

public class ordemOperacoes {
    public static void main (String[] args) {

        // ORDEM DAS OPERAÇÕES [P-E-M-D-A-S]
                             /*
                               P = Parênteses
                               E = Expoentes (potenciação e radiciação)
                               M = Multiplicação
                               D = Divisão
                               A = Adição
                               S = Subtração
                             */

        double resultado = 3 + 4 * (7 - 5) / 2.0;

        System.out.println(resultado);


    }
}
